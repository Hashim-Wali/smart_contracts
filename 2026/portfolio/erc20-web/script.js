// ================================
// 2. Global variables
// ================================

let provider;
let signer;
let tokenContract;

// ================================
// 3. Connect to MetaMask
// ================================

async function connectWallet() {
  // Check if MetaMask exists
  if (typeof window.ethereum === "undefined") {
    alert("MetaMask is not installed");
    return;
  }

  // Request account access
  await window.ethereum.request({ method: "eth_requestAccounts" });

  // Create provider and signer
  provider = new ethers.BrowserProvider(window.ethereum);
  signer = await provider.getSigner();

  const userAddress = await signer.getAddress();
  document.getElementById("address").innerText = userAddress;

  // Create contract instance
  tokenContract = new ethers.Contract(
    contractAddress,
    contractABI,
    signer
  );

  // Load token data
  await loadTokenData(userAddress);
}

// ================================
// 4. Read data from contract
// ================================

async function loadTokenData(userAddress) {
  try {
    // 1. Check if there is even code at this address
    const code = await provider.getCode(contractAddress);
    if (code === "0x") {
      alert("STOP: No contract found at this address on the current network. Check MetaMask network and contractAddress!");
      return;
    }

    // 2. Try to fetch just the name
    const name = await tokenContract.name();
    document.getElementById("name").innerText = name;
    
    // 3. Try to fetch the balance
    const balance = await tokenContract.balanceOf(userAddress);
    document.getElementById("balance").innerText = ethers.formatUnits(balance, 18);

    console.log("Success!");
  } catch (err) {
    console.error("Detailed Error:", err);
    alert("Error: " + err.message);
  }
}

// Add 'any' to allow the provider to detect network shifts
provider = new ethers.BrowserProvider(window.ethereum, "any");
